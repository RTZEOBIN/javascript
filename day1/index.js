// 식별자는 반드시 글자(알파벳 사용)을 건장, 밑줄_, 달로 기호$로 시작을 해야함
// 이 뒤에는 글자, 숫자, 밑줄, 달러 기호를 사용할 수 있음
// 식별자 가장 첫 번째 자리에 숫자를 허용하지 않는 이유는 자바스크립트에서 식별자와 숫자를 쉽게 구별하기 위함
