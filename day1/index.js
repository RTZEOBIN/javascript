// 변수 이름은 어떻게 지어야 하는 지, 주석은 어떻게 만드는 지 , 문과 문을 어떻게 구분하는 지 등을 결정짓는 것들을 프로그래밍 어휘구조라고 한다.
// 자바스크립트에서는 어떤 어휘구조를 가지고 사용하는 지 아래 알아보자.

// 1.1 식별자

