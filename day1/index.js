// 자바스크립트 변수에는 타입이 없다. 보통 자바스크립트는 동적언어 (dynamic language)라고 표현
// 자바스크립트라는 이름 때문에 오해의 소지가 있을 수 있으나, 자바스크립트 몇가지 문법이 자바에서 차용한 건 맞지만,
// 이를 제외하면 두 언어 사이에는 별 다른 관련이 없음. 자바라는 언어와는 확실히 다름

